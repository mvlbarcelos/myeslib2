package org.myeslib.jdbi.function;

import com.google.common.eventbus.EventBus;
import org.myeslib.core.AggregateRoot;
import org.myeslib.core.Command;
import org.myeslib.core.Event;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.util.ArrayList;
import java.util.List;

import static com.google.common.base.Preconditions.checkNotNull;

public class StatefulEventBus {

    private static final Logger logger = LoggerFactory.getLogger(StatefulEventBus.class);
    private final AggregateRoot aggregateRoot;
    private final EventBus bus;
    private final List<Event> events;
    private final List<Command> commands;

    public List<Event> getEvents() {
        bus.unregister(aggregateRoot);
        return events;
    }

    public StatefulEventBus(AggregateRoot aggregateRoot, EventBus bus, List<Event> events, List<Command> commands) {
        checkNotNull(aggregateRoot);
        this.aggregateRoot = aggregateRoot;
        checkNotNull(bus);
        this.bus = bus;
        checkNotNull(events);
        this.events = events;
        checkNotNull(commands);
        this.commands = commands;
        checkNotNull(aggregateRoot);
        bus.register(aggregateRoot);
    }

    public StatefulEventBus(AggregateRoot aggregateRoot, EventBus bus) {
        this.aggregateRoot = aggregateRoot;
        checkNotNull(bus);
        this.bus = bus;
        this.events = new ArrayList<>();
        this.commands = new ArrayList<>();
        checkNotNull(aggregateRoot);
        bus.register(aggregateRoot);
    }

    public void post(Event event) {
        checkNotNull(event);
        logger.info("applying event {} generated by an operation on {}", event, aggregateRoot);
        bus.post(event);
        logger.info("status after event {}", aggregateRoot);
        events.add(event);
    }

    public void add(Command command) {
        checkNotNull(command);
        logger.info("adding command {} generated by an operation on {}", command, aggregateRoot);
        commands.add(command);
    }

}
